import math  # Підключаємо вбудований модуль math для використання математичних функцій (наприклад, добування кореня)


class QuadraticEquation:  # Оголошуємо клас QuadraticEquation, який є шаблоном для створення об'єктів квадратних рівнянь
    def __init__(self, a=0.0, b=0.0,
                 c=0.0):  # Визначаємо конструктор класу, який викликається при створенні нового об'єкта. Значення за замовчуванням - 0.0
        if isinstance(a,
                      QuadraticEquation):  # Перевіряємо, чи переданий перший аргумент 'a' є вже існуючим екземпляром класу QuadraticEquation
            self.a = a.a  # Якщо так, копіюємо значення поля 'a' з переданого об'єкта в поточний (це і є наш "конструктор копіювання")
            self.b = a.b  # Копіюємо значення поля 'b' з переданого об'єкта в поточний об'єкт
            self.c = a.c  # Копіюємо значення поля 'c' з переданого об'єкта в поточний об'єкт
        else:  # Якщо передано не об'єкт, а звичайні значення (або нічого не передано взагалі)
            self.a = float(
                a)  # Перетворюємо передане значення 'a' у дійсне число (float) та записуємо в поле поточного об'єкта
            self.b = float(b)  # Перетворюємо передане значення 'b' у дійсне число та записуємо в поле поточного об'єкта
            self.c = float(c)  # Перетворюємо передане значення 'c' у дійсне число та записуємо в поле поточного об'єкта

    def solve(self):  # Визначаємо загальнодоступний метод solve для обчислення коренів рівняння
        if self.a == 0:  # Перевіряємо, чи дорівнює коефіцієнт 'a' нулю (якщо так, рівняння вже не є квадратним)
            if self.b == 0:  # Якщо 'a' дорівнює 0, перевіряємо, чи дорівнює нулю і коефіцієнт 'b'
                if self.c == 0:  # Якщо всі три коефіцієнти 'a', 'b', 'c' дорівнюють нулю (рівняння вигляду 0 = 0)
                    return float('inf')  # Повертаємо нескінченність, оскільки розв'язком є будь-яке число
                else:  # Якщо 'a'=0, 'b'=0, але 'c' не дорівнює нулю (наприклад, рівняння 5 = 0)
                    return ()  # Повертаємо порожній кортеж, оскільки розв'язків у такого рівняння немає
            else:  # Якщо 'a'=0, але 'b' не дорівнює нулю (отримали звичайне лінійне рівняння bx + c = 0)
                return (
                -self.c / self.b,)  # Обчислюємо єдиний корінь лінійного рівняння і повертаємо його як кортеж з 1 елементом

        discriminant = self.b ** 2 - 4 * self.a * self.c  # Обчислюємо дискримінант за класичною математичною формулою: D = b^2 - 4ac

        if discriminant > 0:  # Перевіряємо, чи дискримінант строго більший за нуль
            x1 = (-self.b + math.sqrt(discriminant)) / (
                        2 * self.a)  # Обчислюємо перший корінь квадратного рівняння за допомогою math.sqrt
            x2 = (-self.b - math.sqrt(discriminant)) / (2 * self.a)  # Обчислюємо другий корінь квадратного рівняння
            return (x1, x2)  # Повертаємо знайдені два корені у вигляді кортежу з двох елементів
        elif discriminant == 0:  # Перевіряємо, чи дискримінант дорівнює рівно нулю
            x = -self.b / (2 * self.a)  # Обчислюємо єдиний (кратний) корінь рівняння
            return (
            x,)  # Повертаємо знайдений корінь у вигляді кортежу з одного елемента (кома обов'язкова для створення кортежу)
        else:  # Якщо всі попередні умови хибні, значить дискримінант від'ємний
            return ()  # Повертаємо порожній кортеж, оскільки дійсних коренів немає

    def show(self):  # Визначаємо метод show для виведення рівняння на екран у зручному для читання форматі
        eq_str = f"{self.a}x^2 + {self.b}x + {self.c} = 0"  # Створюємо базовий текст рівняння, підставляючи значення полів через f-рядок
        eq_str = eq_str.replace("+ -",
                                "- ")  # Замінюємо некрасиві математичні стики (наприклад, '+ -5') на правильні ('- 5')
        print(eq_str)  # Виводимо відформатований рядок у консоль для користувача


def process_equations(filename):  # Визначаємо функцію-менеджер для обробки файлу, яка приймає назву файлу як параметр
    equations = []  # Створюємо порожній список для зберігання всіх об'єктів рівнянь, які ми зчитаємо і створимо з файлу

    try:  # Починаємо блок обробки винятків (щоб програма не "впала", якщо файлу раптом не існує)
        with open(filename,
                  'r') as file:  # Відкриваємо файл з вказаним іменем у режимі читання ('r'). Конструкція 'with' гарантує його автоматичне закриття після роботи
            for line in file:  # Запускаємо цикл, який по черзі зчитує кожен рядок з відкритого текстового файлу
                parts = line.split()  # Розбиваємо зчитаний рядок на окремі слова (числа) за пробілами та записуємо у список 'parts'
                if len(parts) >= 3:  # Перевіряємо, чи вдалося зчитати щонайменше 3 значення (нам потрібні саме a, b, c) з цього рядка
                    eq = QuadraticEquation(parts[0], parts[1], parts[
                        2])  # Створюємо новий об'єкт нашого класу QuadraticEquation, передаючи йому зчитані числа
                    equations.append(
                        eq)  # Додаємо щойно створений екземпляр рівняння у наш загальний список 'equations'
    except FileNotFoundError:  # Якщо система повідомляє, що файл з такою назвою не знайдено
        print(f"Помилка: Файл {filename} не знайдено.")  # Виводимо зрозуміле повідомлення про помилку на екран
        return  # Перериваємо виконання всієї функції, оскільки далі аналізувати нічого

    no_solutions = []  # Створюємо порожній список для зберігання тих рівнянь, які не мають розв'язків
    one_solution = []  # Створюємо порожній список для рівнянь з одним розв'язком (сюди кластимемо кортежі: (рівняння, корінь))
    two_solutions = []  # Створюємо порожній список для рівнянь, які мають рівно два розв'язки
    inf_solutions = []  # Створюємо порожній список для рівнянь з нескінченною кількістю розв'язків

    for eq in equations:  # Запускаємо цикл, який проходить по кожному рівнянню у нашому заповненому списку 'equations'
        roots = eq.solve()  # Викликаємо метод solve() для поточного рівняння і зберігаємо результат його роботи у змінну 'roots'

        if roots == float('inf'):  # Перевіряємо, чи результат повернув значення 'нескінченність'
            inf_solutions.append(eq)  # Якщо так, додаємо поточне рівняння у відповідний список inf_solutions
        elif len(roots) == 0:  # Інакше перевіряємо, чи довжина кортежу результатів дорівнює 0 (тобто розв'язків немає)
            no_solutions.append(eq)  # Якщо так, додаємо поточне рівняння у список no_solutions
        elif len(roots) == 1:  # Інакше перевіряємо, чи довжина кортежу дорівнює 1 (тобто єдиний розв'язок)
            one_solution.append((eq, roots[
                0]))  # Додаємо у список one_solution кортеж з двох частин: само рівняння та значення його єдиного кореня
        elif len(roots) == 2:  # Інакше перевіряємо, чи довжина кортежу дорівнює 2 (два розв'язки)
            two_solutions.append(eq)  # Якщо так, додаємо поточне рівняння у список two_solutions

    print(
        f"\n--- Аналіз файлу {filename} ---")  # Виводимо інформаційний заголовок з назвою файлу, який ми зараз аналізуємо

    print("\n[Не мають розв'язків]:")  # Друкуємо підзаголовок для першої категорії рівнянь
    for eq in no_solutions:  # Проходимо по всіх рівняннях, які потрапили до списку no_solutions
        eq.show()  # Викликаємо метод show() для кожного такого рівняння, щоб надрукувати його на екран

    print("\n[Мають один розв'язок]:")  # Друкуємо підзаголовок для другої категорії
    for eq, root in one_solution:  # Проходимо по списку one_solution, розпаковуючи кожен збережений кортеж у змінні 'eq' (рівняння) та 'root' (корінь)
        eq.show()  # Виводимо на екран саме рівняння
        print(
            f"  -> x = {root:.4f}")  # Відразу під ним виводимо значення його кореня, форматуючи вивід до 4 знаків після коми

    print("\n[Мають два розв'язки]:")  # Друкуємо підзаголовок для третьої категорії
    for eq in two_solutions:  # Проходимо по всіх рівняннях, які потрапили до списку two_solutions
        eq.show()  # Виводимо на екран кожне таке рівняння

    print("\n[Мають нескінченну кількість розв'язків]:")  # Друкуємо підзаголовок для четвертої категорії
    for eq in inf_solutions:  # Проходимо по всіх рівняннях, які потрапили до списку inf_solutions
        eq.show()  # Виводимо їх на екран

    if one_solution:  # Перевіряємо, чи не є порожнім список one_solution (чи знайшли ми хоч одне рівняння з одним коренем)
        min_eq_tuple = min(one_solution, key=lambda item: item[
            1])  # Знаходимо найменший елемент у списку, порівнюючи їх за другим параметром кортежу (за значенням самого кореня)
        max_eq_tuple = max(one_solution, key=lambda item: item[
            1])  # Знаходимо найбільший елемент у списку, використовуючи лямбда-функцію для вказівки критерію порівняння

        print(
            "\n*** Додаткове завдання для рівнянь з 1 розв'язком ***")  # Виводимо заголовок для фінальної частини завдання
        print("Рівняння з НАЙМЕНШИМ розв'язком:")  # Друкуємо пояснювальний текст
        min_eq_tuple[
            0].show()  # Викликаємо метод show() для самого рівняння (воно лежить під індексом 0 у знайденому кортежі)
        print(
            f" (Розв'язок: {min_eq_tuple[1]:.4f})")  # Виводимо значення знайденого мінімального кореня (він лежить під індексом 1)

        print("Рівняння з НАЙБІЛЬШИМ розв'язком:")  # Друкуємо пояснювальний текст
        max_eq_tuple[0].show()  # Викликаємо метод show() для рівняння з найбільшим коренем
        print(f" (Розв'язок: {max_eq_tuple[1]:.4f})")  # Виводимо значення знайденого максимального кореня
    else:  # Якщо список one_solution виявився порожнім (рівнянь з 1 коренем не було у файлі)
        print("\n*** Рівнянь з рівно одним розв'язком не знайдено ***")  # Виводимо відповідне повідомлення
    print("-" * 40 + "\n")  # Малюємо лінію-розділювач для візуального відокремлення результатів роботи з одним файлом

