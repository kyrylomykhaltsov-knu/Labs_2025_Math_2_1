class ProtectedDictIntIterator:
    """
    Кастомний клас-ітератор, спеціально створений для ProtectedDictInt.
    Його єдина відповідальність — повертати ключі словника у порядку зростання.
    """

    def __init__(self, keys_list):
        # Отримуємо список ключів під час створення ітератора.
        # Одразу сортуємо їх за зростанням і зберігаємо всередині.
        self._sorted_keys = sorted(keys_list)

        # Нам потрібен лічильник для відстеження нашої поточної позиції
        # під час перебору. Починаємо з 0 (перший елемент).
        self._current_index = 0

    def __iter__(self):
        """
        Магічний метод __iter__ для ітератора завжди має повертати сам об'єкт ітератора (себе).
        """
        return self

    def __next__(self):
        """
        Цей метод викликається на кожному кроці циклу (наприклад, 'for').
        Він повинен повернути наступний елемент або згенерувати виняток StopIteration.
        """
        # Перевіряємо, чи залишилися ще елементи для перебору
        if self._current_index < len(self._sorted_keys):
            # 1. Беремо ключ за поточним індексом
            key_to_return = self._sorted_keys[self._current_index]

            # 2. Збільшуємо індекс на 1, щоб наступний виклик взяв наступний ключ
            self._current_index += 1

            # 3. Повертаємо знайдений ключ
            return key_to_return
        else:
            # Якщо індекс вийшов за межі списку, сигналізуємо, що ітерацію завершено
            raise StopIteration


