def main():
    print("--- 1. Ініціалізація словника ---")
    my_dict = ProtectedDictInt()

    # Використовуємо перевантажені дужки []
    my_dict[50] = "П'ятдесят"
    my_dict[10] = "Десять"
    my_dict[30] = "Тридцять"
    print(f"Поточний словник: {my_dict}")
    print(f"Кількість елементів: {len(my_dict)}")

    print("\n--- 2. Тестування захисту від змін ---")
    try:
        # Ця операція має викликати помилку, бо ключ 10 вже існує
        my_dict[10] = "Нова десятка"
    except Exception as e:
        print(f"Успішно перехоплено помилку: {e}")

    print("\n--- 3. Тестування математичних операторів ---")
    # Додавання кортежу
    my_dict = my_dict + (20, "Двадцять")
    print(f"Після додавання кортежу (+): {my_dict}")

    # Видалення за ключем
    my_dict = my_dict - 50
    print(f"Після видалення ключа 50 (-): {my_dict}")

    print("\n--- 4. Тестування кастомного ітератора (Завдання 6.2.1) ---")
    # Цикл 'for' неявно викликає iter(my_dict), який повертає ProtectedDictIntIterator.
    # Далі він постійно викликає next(), поки не отримає StopIteration.
    # Виведення має бути відсортовано за ключами: 10, 20, 30.
    for key in my_dict:
        value = my_dict[key]
        print(f"Ключ: {key} -> Значення: {value}")


if __name__ == "__main__":
    main()