import turtle
import math
import time
from random import randint, choice


# === 1. ОПИС КЛАСУ ===
# Створюємо клас Triangle (Трикутник) - шаблон для створення об'єктів-трикутників
class Triangle:
    # Конструктор класу, який викликається при створенні нового трикутника
    def __init__(self, x1, y1, x2, y2):
        # Встановлюємо стартову (опорну) точку трикутника у початок координат (0, 0)
        self.position = (0, 0)
        # Зберігаємо координати другої вершини (відносно стартової точки)
        self.vertex1 = (x1, y1)
        # Зберігаємо координати третьої вершини (відносно стартової точки)
        self.vertex2 = (x2, y2)
        # Встановлюємо базовий колір трикутника (чорний за замовчуванням)
        self.color = "black"

        # Атрибути для анімацій (поворот та розтяг)
        # Початковий кут повороту - 0 радіан (фігура не повернута)
        self.rotation = 0.0
        # Початковий масштаб - 1.0 по X та 1.0 по Y (оригінальний розмір 100%)
        self.scale = (1.0, 1.0)
        # Точка опори (pivot) для перетворень. None означає, що її ще не задано
        self.pivot = None

    # Метод для зміни стартової позиції (переміщення всього трикутника)
    def set_position(self, x, y):
        self.position = (x, y)  # Оновлюємо координати position

    # Метод для зміни кольору заливки трикутника
    def set_color(self, color):
        self.color = color  # Оновлюємо атрибут color

    # Метод для встановлення кута повороту
    def set_rotation(self, rotation):
        self.rotation = rotation  # Зберігаємо нове значення кута в радіанах

    # Метод для встановлення коефіцієнтів розтягування/стискання
    def set_scale(self, scale_x, scale_y):
        self.scale = (scale_x, scale_y)  # Зберігаємо нові коефіцієнти масштабу

    # Метод для встановлення точки, навколо якої буде відбуватися обертання/розтяг
    def set_pivot(self, pivot_x, pivot_y):
        self.pivot = (pivot_x, pivot_y)  # Зберігаємо координати точки опори

    # Допоміжний метод для обчислення реальних (абсолютних) координат на екрані
    def get_absolute_vertices(self):
        # Розпаковуємо координати стартової точки
        x0, y0 = self.position
        # Додаємо відносні координати до стартової точки, щоб отримати реальні координати 2-ї вершини
        x1, y1 = x0 + self.vertex1[0], y0 + self.vertex1[1]
        # Робимо те саме для 3-ї вершини
        x2, y2 = x0 + self.vertex2[0], y0 + self.vertex2[1]
        # Повертаємо три пари координат як результат
        return (x0, y0), (x1, y1), (x2, y2)

    # Метод для знаходження центроїда (точки перетину медіан)
    def get_centroid(self):
        # Отримуємо абсолютні координати всіх трьох вершин
        p0, p1, p2 = self.get_absolute_vertices()
        # Координата X центроїда - це середнє арифметичне всіх X
        cx = (p0[0] + p1[0] + p2[0]) / 3
        # Координата Y центроїда - це середнє арифметичне всіх Y
        cy = (p0[1] + p1[1] + p2[1]) / 3
        # Повертаємо знайдені координати
        return cx, cy

    # Метод для знаходження інцентра (точки перетину бісектрис)
    def get_incenter(self):
        # Отримуємо абсолютні координати
        p0, p1, p2 = self.get_absolute_vertices()
        # Обчислюємо довжину сторони 'a' (між вершинами 1 і 2) за теоремою Піфагора
        a = math.sqrt((p1[0] - p2[0]) ** 2 + (p1[1] - p2[1]) ** 2)
        # Обчислюємо довжину сторони 'b' (між вершинами 0 і 2)
        b = math.sqrt((p0[0] - p2[0]) ** 2 + (p0[1] - p2[1]) ** 2)
        # Обчислюємо довжину сторони 'c' (між вершинами 0 і 1)
        c = math.sqrt((p0[0] - p1[0]) ** 2 + (p0[1] - p1[1]) ** 2)
        # Знаходимо периметр трикутника (сума всіх сторін)
        P = a + b + c

        # Застосовуємо формулу для X-координати інцентра
        ix = (a * p0[0] + b * p1[0] + c * p2[0]) / P
        # Застосовуємо формулу для Y-координати інцентра
        iy = (a * p0[1] + b * p1[1] + c * p2[1]) / P
        # Повертаємо координати центру вписаного кола
        return ix, iy

    # Внутрішній метод для застосування математичних перетворень до однієї точки
    def _transform(self, x, y):
        # Якщо pivot задано, беремо його. Якщо ні - беремо стартову позицію
        px, py = self.pivot if self.pivot is not None else self.position

        # Переносимо точку так, щоб опорна точка опинилася в центрі координат (0,0)
        tx = x - px
        ty = y - py

        # Застосовуємо масштабування (множимо координати на коефіцієнт розтягу)
        tx *= self.scale[0]
        ty *= self.scale[1]

        # Застосовуємо матрицю повороту: обчислюємо новий X через косинус і синус
        rx = tx * math.cos(self.rotation) - ty * math.sin(self.rotation)
        # Обчислюємо новий Y через синус і косинус
        ry = tx * math.sin(self.rotation) + ty * math.cos(self.rotation)

        # Повертаємо точку назад (додаємо координати опорної точки) і видаємо результат
        return px + rx, py + ry

    # Головний метод для малювання трикутника черепашкою
    def draw(self, t):
        # Беремо реальні координати вершин без перетворень
        p0, p1, p2 = self.get_absolute_vertices()

        # Трансформуємо кожну вершину (повертаємо і розтягуємо, якщо задано)
        tp0 = self._transform(*p0)  # Трансформуємо 1-шу вершину
        tp1 = self._transform(*p1)  # Трансформуємо 2-гу вершину
        tp2 = self._transform(*p2)  # Трансформуємо 3-тю вершину

        # Піднімаємо перо черепашки, щоб не малювати лінію під час руху до старту
        t.penup()
        # Переміщуємо черепашку в трансформовану стартову точку
        t.goto(tp0)
        # Опускаємо перо, щоб почати малювати
        t.pendown()

        # Встановлюємо колір заливки фігури
        t.fillcolor(self.color)
        # Даємо команду системі запам'ятовувати контур для подальшої заливки
        t.begin_fill()
        # Малюємо лінію до другої вершини
        t.goto(tp1)
        # Малюємо лінію до третьої вершини
        t.goto(tp2)
        # Повертаємося до першої вершини, замикаючи контур
        t.goto(tp0)
        # Завершуємо заливку (фігура зафарбовується обраним кольором)
        t.end_fill()


# === 2. ОСНОВНА ПРОГРАМА ===

# Створюємо об'єкт екрану turtle
screen = turtle.Screen()
# Встановлюємо білий колір фону
screen.bgcolor("white")

# Створюємо об'єкт "черепашки", яка буде малювати
t = turtle.Turtle()
# Встановлюємо максимальну швидкість для черепашки
t.speed(0)
# Ховаємо курсор черепашки (стрілочку), щоб вона не заважала
t.hideturtle()
# Вимикаємо покадрове оновлення екрану (для миттєвого малювання і плавних анімацій)
turtle.tracer(0)

# Створюємо список можливих кольорів для наших трикутників
colors_list = ["red", "blue", "green", "yellow", "purple", "orange", "cyan", "magenta", "lime", "gold"]

# --- ЗАВДАННЯ 2.2.1 ---
# Виводимо повідомлення в консоль
print("Малюємо 100 випадкових трикутників...")
# Запускаємо цикл, який виконається 100 разів
for _ in range(100):
    # Нескінченний цикл для генерації правильних координат
    while True:
        # Генеруємо випадкові X та Y для двох вершин у межах від -60 до 60
        rand_x1 = randint(-60, 60)
        rand_y1 = randint(-60, 60)
        rand_x2 = randint(-60, 60)
        rand_y2 = randint(-60, 60)
        # Перевіряємо математичну умову існування трикутника (площа != 0)
        if (rand_x1 * rand_y2) - (rand_x2 * rand_y1) != 0:
            # Якщо трикутник справжній, зупиняємо цикл генерації
            break

    # Створюємо об'єкт Triangle зі згенерованими валідними координатами
    my_triangle = Triangle(rand_x1, rand_y1, rand_x2, rand_y2)
    # Встановлюємо випадкову позицію на екрані (в межах розмірів вікна)
    my_triangle.set_position(randint(-350, 350), randint(-300, 300))
    # Вибираємо випадковий колір зі списку та встановлюємо його
    my_triangle.set_color(choice(colors_list))
    # Передаємо черепашку (t) в метод малювання, щоб відобразити фігуру
    my_triangle.draw(t)

# Оновлюємо екран, щоб показати всі 100 намальованих трикутників одразу
turtle.update()
# Робимо паузу програми на 2 секунди
time.sleep(2)
# Очищаємо екран (видаляємо всі малюнки) для наступного етапу
t.clear()

# --- ПІДГОТОВКА ДО АНІМАЦІЙ ---
# Створюємо новий великий трикутник для демонстрації анімацій
anim_tri = Triangle(120, 0, 60, 160)
# Ставимо його рівно в центр екрану
anim_tri.set_position(0, 0)

# --- ЗАВДАННЯ 2.2.2 (Обертання) ---
print("Анімація 1: Обертання навколо стартової вершини")
# Встановлюємо колір для першої анімації
anim_tri.set_color("cyan")
# Передаємо координати стартової точки як точку опори для обертання
anim_tri.set_pivot(*anim_tri.position)
# Цикл для кутів від 0 до 360 градусів з кроком 5
for angle in range(0, 360, 5):
    # Стирання попереднього кадру
    t.clear()
    # Конвертуємо градуси в радіани і встановлюємо кут
    anim_tri.set_rotation(math.radians(angle))
    # Малюємо трикутник з новим кутом
    anim_tri.draw(t)
    # Оновлюємо вікно, щоб показати новий кадр
    turtle.update()
    # Мікропауза 0.01 сек для плавності обертання
    time.sleep(0.01)
# Після циклу скидаємо обертання до нуля
anim_tri.set_rotation(0)

# --- ЗАВДАННЯ 2.2.2 (Розтягування) ---
print("Анімація 2: Розтягування відносно стартової вершини")
# Змінюємо колір
anim_tri.set_color("magenta")
# Цикл генерує числа від 10 до 19, а потім від 19 до 11 (для пульсації: туди-назад)
for i in list(range(10, 20)) + list(range(20, 10, -1)):
    # Стирання кадру
    t.clear()
    # Рахуємо масштаб (наприклад, 15 / 10 = 1.5)
    scale_val = i / 10.0
    # Встановлюємо новий масштаб по X та Y
    anim_tri.set_scale(scale_val, scale_val)
    # Малюємо
    anim_tri.draw(t)
    # Оновлюємо екран
    turtle.update()
    # Пауза для плавності
    time.sleep(0.04)
# Скидаємо масштаб до оригінального (1.0)
anim_tri.set_scale(1.0, 1.0)

# --- ЗАВДАННЯ 2.2.3 (Обертання - Бісектриси) ---
print("Анімація 3: Обертання навколо точки перетину бісектрис")
# Змінюємо колір
anim_tri.set_color("gold")
# Обчислюємо координати інцентра за допомогою нашого методу
incenter = anim_tri.get_incenter()
# Встановлюємо знайдений інцентр як точку опори
anim_tri.set_pivot(*incenter)
# Запускаємо цикл обертання (від 0 до 360)
for angle in range(0, 360, 5):
    t.clear()
    anim_tri.set_rotation(math.radians(angle))
    anim_tri.draw(t)
    # Додатково: піднімаємо перо
    t.penup()
    # Йдемо в точку інцентра
    t.goto(incenter)
    # Малюємо червону крапку розміром 8, щоб візуально показати вісь обертання
    t.dot(8, "red")
    # Оновлюємо екран і чекаємо
    turtle.update()
    time.sleep(0.01)
# Скидаємо поворот
anim_tri.set_rotation(0)

# --- ЗАВДАННЯ 2.2.3 (Розтягування - Медіани) ---
print("Анімація 4: Розтягування відносно точки перетину медіан")
anim_tri.set_color("lime")
# Обчислюємо координати центроїда
centroid = anim_tri.get_centroid()
# Встановлюємо центроїд як точку опори
anim_tri.set_pivot(*centroid)
# Запускаємо цикл розтягування (пульсація)
for i in list(range(10, 20)) + list(range(20, 10, -1)):
    t.clear()
    scale_val = i / 10.0
    anim_tri.set_scale(scale_val, scale_val)
    anim_tri.draw(t)
    # Малюємо синю крапку в центроїді, щоб показати точку, відносно якої йде розтяг
    t.penup()
    t.goto(centroid)
    t.dot(8, "blue")

    turtle.update()
    time.sleep(0.04)

# Повідомляємо в консоль про завершення
print("Демонстрацію завершено. Клікніть на вікно, щоб закрити.")
# Залишаємо вікно відкритим, доки користувач не клікне по ньому мишкою
screen.exitonclick()