import turtle
from random import randint, choice


# === 1. ОПИС КЛАСУ ===
class Triangle:
    def __init__(self, x1, y1, x2, y2):
        # Стартова точка трикутника (за замовчуванням центр координат)
        self.position = (0, 0)

        # Відносні координати двох інших вершин
        # (відносно стартової точки position)
        self.vertex1 = (x1, y1)
        self.vertex2 = (x2, y2)

        # Колір за замовчуванням
        self.color = "black"

    def set_position(self, x, y):
        # Метод для зміни стартової позиції трикутника
        self.position = (x, y)

    def set_color(self, color):
        # Метод для зміни кольору трикутника
        self.color = color

    # ДОДАНИЙ МЕТОД: Без нього трикутник не намалюється!
    def draw(self, t):
        """
        Метод приймає об'єкт черепашки (t) і малює трикутник
        на основі збережених координат.
        """
        # Розпаковуємо координати для зручності
        x0, y0 = self.position
        dx1, dy1 = self.vertex1
        dx2, dy2 = self.vertex2

        # Абсолютні координати вершин (стартова точка + відносний зсув)
        point1 = (x0 + dx1, y0 + dy1)
        point2 = (x0 + dx2, y0 + dy2)

        # Починаємо малювати
        t.penup()  # Піднімаємо перо, щоб не залишати слід під час переміщення
        t.goto(x0, y0)  # Йдемо в стартову точку
        t.pendown()  # Опускаємо перо

        t.fillcolor(self.color)  # Встановлюємо колір заливки
        t.begin_fill()  # Даємо команду "почати заливку"

        t.goto(point1)  # Малюємо лінію до другої вершини
        t.goto(point2)  # Малюємо лінію до третьої вершини
        t.goto(x0, y0)  # Повертаємось у стартову точку, замикаючи трикутник

        t.end_fill()  # Завершуємо заливку (фігура зафарбовується)


# === 2. ОСНОВНА ПРОГРАМА ===

# Налаштування вікна та черепашки
screen = turtle.Screen()
screen.bgcolor("white")

t = turtle.Turtle()
t.speed(0)  # 0 - це максимальна швидкість малювання, щоб не чекати довго
t.hideturtle()  # Ховаємо саму "черепашку" (курсор), щоб бачити лише фігури

# Список кольорів (можна взяти з документації trinket.io)
colors_list = ["red", "blue", "green", "yellow", "purple", "orange", "pink", "cyan", "magenta", "lime", "gold",
               "tomato"]

# Цикл для малювання 100 трикутників
for _ in range(100):
    # 1. Генеруємо випадкову форму (відносні координати вершин)
    # Беремо невеликі числа, щоб трикутники не були гігантськими
    while True:
        rand_x1 = randint(-60, 60)
        rand_y1 = randint(-60, 60)
        rand_x2 = randint(-60, 60)
        rand_y2 = randint(-60, 60)

        # Перевірка умови існування трикутника (площа не дорівнює нулю)
        if (rand_x1 * rand_y2) - (rand_x2 * rand_y1) != 0:
            break  # Якщо трикутник існує, виходимо з циклу генерації

    # Створюємо об'єкт (екземпляр) нашого класу
    my_triangle = Triangle(rand_x1, rand_y1, rand_x2, rand_y2)

    # 2. Генеруємо випадкову позицію на екрані
    # Екран зазвичай має координати від -300 до 300 по осях
    pos_x = randint(-350, 350)
    pos_y = randint(-300, 300)
    my_triangle.set_position(pos_x, pos_y)  # Встановлюємо позицію

    # 3. Вибираємо випадковий колір зі списку за допомогою функції choice
    random_color = choice(colors_list)
    my_triangle.set_color(random_color)  # Встановлюємо колір

    # 4. Малюємо створений трикутник
    my_triangle.draw(t)

# Завершення роботи: вікно не закриється, поки ви на нього не натиснете
turtle.done()