class StringCalculator:
    # Конструктор класу: викликається при створенні нового об'єкта
    def __init__(self):
        # Поле для зберігання самого виразу у вигляді рядка
        self.expression = ""
        # Словник для зберігання параметрів (наприклад: {'x': 10, 'y': 5})
        self.parameters = {}

        # а) Метод для задавання арифметичного виразу

    def set_expression(self, expr):
        # Зберігаємо вираз, попутно видаляючи всі пробіли для зручності обробки
        self.expression = expr.replace(" ", "")

    # b) Метод для задавання значень параметрів
    def set_parameter(self, name, value):
        # Додаємо або оновлюємо значення параметра у нашому словнику
        self.parameters[name] = value

    # Допоміжний метод (прихований від користувача), який розбиває рядок на елементи
    def _get_tokens(self):
        tokens = []  # Список, де будуть зберігатися числа, змінні та знаки
        current_item = ""  # Тимчасова змінна для накопичення символів (наприклад, щоб зібрати число "15" з "1" та "5")

        # Проходимося по кожному символу у нашому виразі
        for char in self.expression:
            # Якщо символ — це один з математичних знаків
            if char in "+-*/":
                # Якщо ми вже накопичили якесь число або змінну, додаємо її до списку
                if current_item != "":
                    tokens.append(current_item)
                    current_item = ""  # Очищаємо тимчасову змінну
                # Додаємо сам математичний знак до списку
                tokens.append(char)
            else:
                # Якщо це літера або цифра, додаємо її до тимчасової змінної
                current_item += char

        # Після завершення циклу, якщо в тимчасовій змінній щось залишилось (останнє число), додаємо його
        if current_item != "":
            tokens.append(current_item)

        return tokens

    # c) Метод для перевірки, чи для всіх параметрів задано значення
    def check_parameters(self):
        # Отримуємо список усіх елементів виразу
        tokens = self._get_tokens()

        # Перевіряємо кожен елемент
        for token in tokens:
            # Метод isalpha() перевіряє, чи складається рядок тільки з літер (чи це параметр)
            if token.isalpha():
                # Якщо параметр є у виразі, але його немає в нашому словнику
                if token not in self.parameters:
                    return False  # Повертаємо False (не всі задані)

        return True  # Якщо цикл пройшов успішно, значить всі параметри задані

    # d) Метод для обчислення значення виразу
    def calculate(self):
        # Спочатку перевіряємо, чи можемо ми взагалі щось рахувати
        if not self.check_parameters():
            return "Помилка: Не всім параметрам задано значення!"

        tokens = self._get_tokens()

        # Якщо вираз порожній, повертаємо 0
        if len(tokens) == 0:
            return 0

        # Допоміжна функція, яка перетворює рядок ('5' або 'a') на справжнє число (float)
        def get_value(t):
            if t.isalpha():  # Якщо це літера
                return float(self.parameters[t])  # Беремо значення зі словника
            else:
                return float(t)  # Якщо це просто число у вигляді рядка, робимо його дробовим числом

        # Беремо перше число (або значення першого параметра) як початковий результат
        result = get_value(tokens[0])

        # Починаємо з індексу 1 (де знаходиться перший математичний знак)
        i = 1
        # Проходимося по списку, роблячи кроки по 2 (знак, потім наступне число)
        while i < len(tokens):
            operator = tokens[i]  # Беремо знак (+, -, *, /)
            next_value = get_value(tokens[i + 1])  # Беремо наступне число

            # Виконуємо відповідну дію та перезаписуємо результат (ігноруючи пріоритет дій!)
            if operator == '+':
                result += next_value
            elif operator == '-':
                result -= next_value
            elif operator == '*':
                result *= next_value
            elif operator == '/':
                # Перевірка, щоб не зламати програму діленням на нуль
                if next_value == 0:
                    return "Помилка: Ділення на нуль!"
                result /= next_value

            i += 2  # Переходимо до наступного знака (перестрибуємо через число)

        return result


# --- Демонстрація роботи ---
if __name__ == "__main__":
    # Створюємо екземпляр нашого калькулятора
    calc = StringCalculator()

    # a) Задаємо вираз
    calc.set_expression("2 + 3 * x - y")

    # b) Задаємо параметри
    calc.set_parameter("x", 4)
    # Зверніть увагу: y ми поки не задали

    # c) Перевіряємо параметри
    print("Чи всі параметри задані?", calc.check_parameters())  # Виведе: False

    # Спроба обчислити (має видати помилку)
    print("Спроба 1:", calc.calculate())

    # Задаємо параметр y
    calc.set_parameter("y", 5)

    print("Чи всі параметри задані тепер?", calc.check_parameters())  # Виведе: True

    # d) Обчислення
    # Логіка (без пріоритету): 2 + 3 = 5; 5 * 4 = 20; 20 - 5 = 15.
    print("Результат обчислення:", calc.calculate())  # Виведе: 15.0